public class DataTypeEX05 {
    public static void main(String[] args){
        
        // 정수형
        int i = 1;
        System.out.println(i);
        
        // byte b=1;
        // short s=1;
        
        // long l1=1;
        long l2=1L; //명시적으로 롱 형태로 쓸거라고 적은거 L
        System.out.println(l2);
        
        // 진수(2,8,16)
        
        int i2=0b1010;
        int i8=030;
        int i16=0xA4;
        System.out.println(i2);
        System.out.println(i8);
        System.out.println(i16);
        

    }
}
