public class variableEX01 {
    public static void main(String[] args){
        // 프로그램 시작

        
        int intNum1; // 변수 선언 = 자료형 변수명;
        intNum1 = 10; // 변수의 초기화
        System.out.println(intNum1); // 변수 내용의 출력
        
        int intNum2 = 20; //변수 선언과 동시에 초기화
        System.out.println(intNum2); // 변수 내용의 출력
        
        intNum1 = 100; // 변수값 재할당
        System.out.println(intNum1); // 변수 내용의 출력

        // 프로그램 끝
    }
}
